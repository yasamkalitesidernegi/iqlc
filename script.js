let currentLang = "tr";
let currentSlide = 0;
let slideTimer = null;

function renderTopics() {
  const grid = document.getElementById("themesGrid");
  grid.innerHTML = "";
  data[currentLang].themes.items.forEach(item => {
    const card = document.createElement("div");
    card.className = "topic-card";
    card.textContent = item;
    grid.appendChild(card);
  });
}

function renderPubs() {
  const grid = document.getElementById("publicationGrid");
  grid.innerHTML = "";
  data[currentLang].pubs.items.forEach((item, index) => {
    const card = document.createElement("article");
    card.className = "pub-card";
    card.innerHTML = `
      <div class="pub-number">${index + 1}</div>
      <h3>${item.title}</h3>
      <p>${item.text}</p>
    `;
    grid.appendChild(card);
  });
}

function renderBoardList(id, items) {
  const el = document.getElementById(id);
  el.innerHTML = "";
  items.forEach(item => {
    const row = document.createElement("div");
    row.className = "board-item";
    row.textContent = item;
    el.appendChild(row);
  });
}

function applyTranslations() {
  document.documentElement.lang = currentLang;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const path = el.getAttribute("data-i18n").split(".");
    let value = data[currentLang];
    path.forEach(k => value = value?.[k]);
    if (typeof value === "string") el.textContent = value;
  });

  renderTopics();
  renderPubs();
  renderBoardList("presidentList", data.boardMembers.president);
  renderBoardList("advisoryList", data.boardMembers.advisory);
  renderBoardList("organizingList", data.boardMembers.organizing);
  renderBoardList("scientificList", data.boardMembers.scientific);

  document.querySelectorAll("[data-lang-btn]").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.langBtn === currentLang);
  });
}

function goToSlide(index) {
  const slides = document.querySelectorAll(".hero-slide");
  const dots = document.querySelectorAll(".dot");
  currentSlide = (index + slides.length) % slides.length;
  slides.forEach((slide, i) => slide.classList.toggle("active", i === currentSlide));
  dots.forEach((dot, i) => dot.classList.toggle("active", i === currentSlide));
}

function startSlider() {
  clearInterval(slideTimer);
  slideTimer = setInterval(() => goToSlide(currentSlide + 1), 4500);
}

document.addEventListener("DOMContentLoaded", () => {
  applyTranslations();
  goToSlide(0);
  startSlider();

  document.querySelectorAll("[data-lang-btn]").forEach(btn => {
    btn.addEventListener("click", () => {
      currentLang = btn.dataset.langBtn;
      applyTranslations();
    });
  });

  document.querySelectorAll(".dot").forEach(dot => {
    dot.addEventListener("click", () => {
      goToSlide(Number(dot.dataset.slide));
      startSlider();
    });
  });
});
