const html = document.documentElement;
const buttons = document.querySelectorAll('.lang-btn');
const translatable = document.querySelectorAll('[data-i18n]');

function setLanguage(lang) {
  html.lang = lang;
  localStorage.setItem('iqlc-lang', lang);
  buttons.forEach(btn => btn.classList.toggle('active', btn.dataset.setlang === lang));
  translatable.forEach(el => {
    const key = el.dataset.i18n;
    if (translations[lang]?.[key]) el.textContent = translations[lang][key];
  });
}

buttons.forEach(btn => btn.addEventListener('click', () => setLanguage(btn.dataset.setlang)));
setLanguage(localStorage.getItem('iqlc-lang') || 'tr');

const menuToggle = document.querySelector('.menu-toggle');
const navLinks = document.querySelector('.nav-links');
menuToggle?.addEventListener('click', () => navLinks.classList.toggle('open'));
navLinks?.querySelectorAll('a').forEach(link => link.addEventListener('click', () => navLinks.classList.remove('open')));

const targetDate = new Date('2026-01-16T09:00:00+03:00').getTime();
const countdownIds = { days: 'days', hours: 'hours', minutes: 'minutes', seconds: 'seconds' };

function updateCountdown() {
  const now = Date.now();
  const distance = targetDate - now;
  if (distance <= 0) {
    Object.values(countdownIds).forEach(id => document.getElementById(id).textContent = '00');
    return;
  }
  const values = {
    days: Math.floor(distance / (1000 * 60 * 60 * 24)),
    hours: Math.floor((distance / (1000 * 60 * 60)) % 24),
    minutes: Math.floor((distance / (1000 * 60)) % 60),
    seconds: Math.floor((distance / 1000) % 60)
  };
  for (const [key, value] of Object.entries(values)) {
    document.getElementById(countdownIds[key]).textContent = String(value).padStart(2, '0');
  }
}
updateCountdown();
setInterval(updateCountdown, 1000);

const observer = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) entry.target.classList.add('visible');
  });
}, { threshold: 0.14 });

document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
